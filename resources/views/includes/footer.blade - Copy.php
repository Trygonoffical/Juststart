<script>
function getCookie(name) {
    let nameEQ = name + "=";
    let ca = document.cookie.split(';');
    for(let i = 0; i < ca.length; i++) {
        let c = ca[i];
        while (c.charAt(0) == ' ') c = c.substring(1, c.length);
        if (c.indexOf(nameEQ) == 0) return c.substring(nameEQ.length, c.length);
    }
    return null;
}

function setCookie(name, value, days) {
    let expires = "";
    if (days) {
        let date = new Date();
        date.setTime(date.getTime() + (days * 24 * 60 * 60 * 1000));
        expires = "; expires=" + date.toUTCString();
    }
    document.cookie = name + "=" + (value || "") + expires + "; path=/";
}

function eraseCookie(name) {
    document.cookie = name + '=; Max-Age=-99999999;';
}

function display_cta(){
  let cto_cookie = getCookie("cto_cookie");

  if (cto_cookie == null) {
    document.getElementById('footer-cto-modal').style.display = 'block';
  }
}

$(document).ready(function(){
  display_cta();
  
  $(document).on('click', '#ctoAcceptCookie', function(){
    setCookie("cto_cookie", 'true', 1);
    document.getElementById('footer-cto-modal').style.display = 'none';
  })

  $(document).on('click', '.ctoClose', function(){
    document.getElementById('footer-cto-modal').style.display = 'none';
  })

})
</script>
